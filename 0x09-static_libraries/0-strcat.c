/**
 * _strcat - concatenates two strings
 * @dest: input value
 * @src: input value
 *
 * Return: void
 */
char *_strcat(char *dest, char *src)
{
	int i;
	int j;
	/*dest is a pointer to destinatition*/
	/*src parameter points to the source*/
	i = 0;
	while (dest[i] != '\0')
	{
		i++;	/*loop enters increment i until null*/
	}
	j = 0;
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}

	dest[i] = '\0';
	return (dest);
}
